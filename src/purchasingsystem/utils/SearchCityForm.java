    private void formatoTabla(){            //Ajusta el ancho de las columnas

        int[] weights = {1,164,1,134};
            
        for(int i = 0; i < tabCitys.getColumnCount(); i++){
            tabCitys.getColumnModel().getColumn(i).setMinWidth(weights[i]);
            tabCitys.getColumnModel().getColumn(i).setPreferredWidth(weights[i]);
        }
        
        DefaultTableCellRenderer headerRenderer = new DefaultTableCellRenderer();
        headerRenderer.setBackground(new Color(255, 215, 0));
        headerRenderer.setForeground(Color.BLACK);

        
        for (int i = 0; i < tabCitys.getModel().getColumnCount(); i++) {          //Recorro y se lo aplico a cada header
            tabCitys.getColumnModel().getColumn(i).setHeaderRenderer(headerRenderer);
            
        }
        
    }
    
